--[[
 fluent_ip_to_simion_pas.lua
 Converts ANSYS Fluent Interpolation (.ip) file to SIMION PA files.
 
 The .ip file contains Fluent calculated flow data parameters (e.g.
 x/y/z coordinates of mesh points, x/y/z components of velocity, pressure
 and/or temperature) after interpolating onto some mesh.
 For SIMION purposes, we prefer this mesh to be a uniform rectilinear
 grid to allow easy representation as SIMION PA files.
 The .ip file can be generated by "File > Interpolate" in Fluent.

 This will create one SIMION PA for each flow parameter.  Any points not
 defined in the Fluent file currently default to 0.
 
 Some adjustment to this program may be necessary for your own purposes.
 
 WARNING: Fluent defaults to SI units, but the user may change this.
 SIMION units are by default mm for distance and
 mm/microsecond for velocity.  See SIMION SDS and HS1 for flow units
 they use.  This program makes some attempt to convert these, but
 you may need to rescale units.
 
 WARNING: For best precision, take care about whether parameters
 represent the corners or centers of grid cells.  Flows may be
 off by one grid unit if you don't accounts for this when
 creating PA's and interpreting them in SIMION.  So, please check
 if maximum precision is desired.

 WARNING: In the Fluent file, the x/y/z coordinates of the points are
 not necessarily in lexographic order nor fill the entire rectangular array.
 More significantly, the points tend to not exactly align to the
 rectilinear grid; rather, points that you would expect to have identical
 values of some x/y/z coordinate actually tend to have a small variance
 in that coordinate.
 Therefore, to interpret this data file, we resort to some heuristics to
 define how to round to the nearest grid point.
 
 A typical way to plot velocity vectors (using "contourlib81" in the "contour"
 example), assuming the vx,vy,vz arrays are PA instances #1-3 on the PA
 Instances list on the View screen PAs tab, is as follows
 (in SIMION >= 8.1.1.0):
 
   local vxinst = simion.wb.instances[1]
   local vyinst = simion.wb.instances[2]
   local vzinst = simion.wb.instances[3]
   local function velocity(x,y,z)
     return vxinst:potential_wc(x,y,z),
            vyinst:potential_wc(x,y,z),
            vzinst:potential_wc(x,y,z)
   end
   simion.import'../contour/contourlib81.lua'.plot{func=velocity, mark=true}
 
 D.Manura,2012-07-16,2012-03.
 (c) 2012 Scientific Instrument Services, Inc. (Licensed SIMION 8.1)
--]]

-- Name of Fluent input file.
local filename = 'fluent_example.ip'

-- Open Fluent file.
local fh = assert(io.open(filename, 'r'))

-- Load header from .ip file.
local fluent_version = fh:read'*n'
local dimension = fh:read'*n'
local num_points = fh:read'*n'
local num_fields = fh:read'*n'
local field_names = {'x', 'y'}
if dimension == 3 then field_names[#field_names+1] = 'z' end
field_names.start = #field_names + 1
fh:read'*l' -- eat newline
for i=1,num_fields do
  local name = fh:read'*l'
  field_names[#field_names+1] = name
end

-- Dump headers (debugging).
print('Fluent file version:', fluent_version)
    -- expects 1 (Fluent 5) or 2 (Fluent 6)
print('Dimension:', dimension)
print('Num points:', num_points)
print('Fields:', num_fields)
for i=1,num_fields do
  print('', field_names[i])
end

-- Read data points from .ip file.
local fields = {}
for i=1,#field_names do
  local data = {}
  for j=1,num_points do
    local num = assert(fh:read'*n')
    data[#data+1] = num
  end
  fields[field_names[i]] = data  
end
local rest = fh:read'*a'
if not rest:match'^%s*$' then
  error('extra data found at end of file: ['..rest..']')
end

-- Close Fluent file.
fh:close()

-- If you wish to swap coordinates, you could do things
-- like this here:
--[[
fields.x, fields.z = fields.z, fields.x
fields['x-velocity'], fields['z-velocity'] = fields['z-velocity'], fields['x-velocity']
--]]

