-- Get unique, sorted list of values for each x/y/z coordinate.
local function sort_coords(t)
  local tt = {}
  local found = {}
  for _,v in ipairs(t) do
    if not found[v] then
      tt[#tt+1] = v
      found[v] = true
    end
  end
  table.sort(tt)
  return tt
end
local xs = sort_coords(fields.x)
local ys = sort_coords(fields.y)
local zs = sort_coords(fields.z or {0})

--[[
-- Dump unique, sorted coordinates.
print 'xs'
for i,v in ipairs(xs) do print(v) end
print 'ys'
for i,v in ipairs(ys) do print(v) end
print 'zs'
for i,v in ipairs(zs) do print(v) end
