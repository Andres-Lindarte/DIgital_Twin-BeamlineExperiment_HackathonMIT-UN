-- Rounds value to nearest integer.
local function round(x)
  return math.floor(x + 0.5)
end

-- Compute statistics for coordinate.
-- `xmin` and `xmax` and min and max values of coordinate respectively.
-- `dx` is approximate distance between successive values.
-- `npoints` is number of data points.
-- WARNING: uses heuristics to determine grid and might not work in all cases.
local function coord_stats(t)
  local min = t[1]
  local max = t[#t]
  -- Find maximum distance between points.
  local dmax = 0
  for i=1,#t do
    if i > 1 then
      local du = t[i] - t[i-1]
      dmax = math.max(dmax, du)
    end
  end
  if dmax == 0 then
    return min, max, 1E+100, 1
  else
    -- Find average distance between points close to maximum distance.
    -- There tends to be some variation from the maximum.
    local dsum, dcount = 0, 0
    for i=1,#t do
      if i > 1 then
        local du = t[i] - t[i-1]
        if math.abs(math.abs(du / dmax) - 1) < 0.1 then
          dsum = dsum + du
          dcount = dcount + 1
        end
      end
    end
    local dave = dsum / dcount
    local offset = ((max-min)/dave + 0.5) % 1 - 0.5
    if math.abs(offset) > 0.1 then  -- allow small fraction of gu error
      error('error: offset='..offset)
    end
    -- Finish stats.
    local npoints = round((max - min) / dmax) + 1
    local d = npoints == 1 and 1E+100 or (max-min)/(npoints-1)
    return min, max, d, npoints
  end
end
local xmin, xmax, dx, nx = coord_stats(xs)
local ymin, ymax, dy, ny = coord_stats(ys)
local zmin, zmax, dz, nz = coord_stats(zs)

-- Dump coordinate stats.
print('nx=', nx, 'xmin=', xmin, 'xmax=', xmax, 'dx=', dx)
print('ny=', ny, 'ymin=', ymin, 'ymax=', ymax, 'dy=', dy)
print('nz=', nz, 'zmin=', zmin, 'zmax=', zmax, 'dz=', dz)

if nx*ny*nz < num_points then  -- note: Fluent may omit points
  error(('Failed to intepret grid size: %d x %d x %d < %d'):format(
    nx,ny,nz, num_points))
end

-- Force rotation if coordinate other than z has one point.
-- SIMION PA requires nz == 1 if array has 2D symmetry.
if (nx == 0 and 1 or 0) + (ny == 0 and 1 or 0) + (nz == 0 and 1 or 0) > 1 then
  error('multiple coordinates have only one point.')
end
if nx == 1 then
  print 'WARNING: forcing rotation: y,z,x -> x,y,z so that nz=1'
  fields.x, fields.y, fields.z =
  fields.y, fields.z, fields.x
  xmin,xmax,dx,nx, ymin,ymax,dy,ny, zmin,zmax,dz,nz =
  ymin,ymax,dy,ny, zmin,zmax,dz,nz, xmin,xmax,dx,nx
  assert(nz == 1)
elseif ny == 1 then
  print 'WARNING: forcing rotation: z,x,y -> x,y,z so that nz=1'
  fields.x, fields.y, fields.z =
  fields.z, fields.x, fields.y
  xmin,xmax,dx,nx, ymin,ymax,dy,ny, zmin,zmax,dz,nz =
  zmin,zmax,dz,nz, xmin,xmax,dx,nx, ymin,ymax,dy,ny
  assert(nz == 1)
end

-- Create PA object headers.
local pas = {}
local MM_PER_M = 1000 -- assuming positions in original file in units of m.
for i=field_names.start, #field_names do
  local pa = simion.pas:open()
  pa:size(nx,ny,nz)
  pa.filename = field_names[i] .. '.pa'  -- note: you may wish to change this
  pa.refinable = false  -- prevent SIMION prompting to refine this PA.
  pa.dx_mm = MM_PER_M * dx
  pa.dy_mm = MM_PER_M * dy
  if nz > 1 then pa.dz_mm = MM_PER_M * dz end
  
  -- optionally enable mirroring like this:
  --pa.mirror_x = true
  
  pas[#pas+1] = pa
end

-- Gets integer index for given real number coordinate value `u`,
-- assuming `umin` is value at index 0 and `du` is the approximate
-- difference in `u` between successive indices.
local function coord_round(u, umin, du)
  local offset = ((u - umin)/du + 0.5) % 1 - 0.5
  if math.abs(offset) > 0.1 then -- off by >10% from grid point.
    error(('ASSERT: position %g is %d % away from grid point'):format(
          u, offset*100))
  end
  return round((u - umin)/du)
end

-- Fill PA data points.
for i=1,#fields.x do
  local xi = coord_round(fields.x[i], xmin, dx)
  local yi = coord_round(fields.y[i], ymin, dy)
  local zi = nz == 1 and 0 or coord_round(fields.z[i], zmin, dz)
  for j=field_names.start,#field_names do
    local name = field_names[j]
    pas[j-field_names.start+1]:potential(xi,yi,zi, fields[name][i])
  end
end

-- Save PA's.
for i=1,#pas do
  pas[i]:save()
end

print 'DONE'
